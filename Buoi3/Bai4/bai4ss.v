module bai4ss(
    input [9:0] SW,
    output [2:0] LEDR
);

// tach A va B
wire [4:0] A = SW[9:5];
wire [4:0] B = SW[4:0];

// A = B
assign LEDR[1] = (A == B);

// A > B
assign LEDR[2] =
    (A[4] & ~B[4]) |
    ((A[4] ~^ B[4]) & A[3] & ~B[3]) |
    ((A[4] ~^ B[4]) & (A[3] ~^ B[3]) & A[2] & ~B[2]) |
    ((A[4] ~^ B[4]) & (A[3] ~^ B[3]) & (A[2] ~^ B[2]) & A[1] & ~B[1]) |
    ((A[4] ~^ B[4]) & (A[3] ~^ B[3]) & (A[2] ~^ B[2]) & (A[1] ~^ B[1]) & A[0] & ~B[0]);

// A < B
assign LEDR[0] =
    (~A[4] & B[4]) |
    ((A[4] ~^ B[4]) & ~A[3] & B[3]) |
    ((A[4] ~^ B[4]) & (A[3] ~^ B[3]) & ~A[2] & B[2]) |
    ((A[4] ~^ B[4]) & (A[3] ~^ B[3]) & (A[2] ~^ B[2]) & ~A[1] & B[1]) |
    ((A[4] ~^ B[4]) & (A[3] ~^ B[3]) & (A[2] ~^ B[2]) & (A[1] ~^ B[1]) & ~A[0] & B[0]);

endmodule